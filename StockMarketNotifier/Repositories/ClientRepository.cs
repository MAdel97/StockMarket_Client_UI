using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

using Microsoft.Extensions.Configuration;
using StockMarketNotifier.Interfaces;
using StockMarketNotifier.Common;
using StockMarketNotifier.Models;
using Microsoft.EntityFrameworkCore;
using StockUpdates.Context;

namespace StockMarketNotifier.Repositories
{
    public class ClientRepository : IClientRepository
    {
        private StockUpdatesContext _context = null;
        private DbSet<Client> _clients = null;
        private readonly IConfiguration _configuration;


        public ClientRepository()
        {
            this._context = new StockUpdatesContext();

            _clients = _context.Set<Client>();
        }

        public async Task<Client> AddClient(Client _obj)
        {

            DateTime DateTime_ = DateTime.Now;

            using (var transaction = _context.Database.BeginTransaction())
            {
                try
                {


                    _context.Add(_obj);
                    await _context.SaveChangesAsync();
                    transaction.CommitAsync();
                }


                catch (Exception ex)
                {


                    Console.Write("An error occured");
                }
            }
            return _obj;
        }


        public async Task<List<Client>> GetClients()
        {

            var list = new List<Client>();
            DateTime DateTime_ = DateTime.Now;

            try
            {


                list = await _context.Clients.ToListAsync();

            }
            catch (Exception ex)
            {
                Console.Write("An error occured");
            }

            return list;
        }


      


        public async Task<Client> UpdateClient(Client client,int ID)
        {

            //var list = new List<AcademicCourse>();
            DateTime DateTime_ = DateTime.Now;
            ///var course = new AcademicCourse();
            var result = new Client();
            try
            {
                client.Id= ID;

                result = _clients.Update(client).Entity;
                _context.SaveChanges();

            }
            catch (Exception ex)
            {
                Console.Write("An error occured");

            }
            return result;


        }


        public async Task<Client> DeleteClient(int CourseId)
        {

            //var list = new List<AcademicCourse>();
            DateTime DateTime_ = DateTime.Now;
            ///var course = new AcademicCourse();
            var result = new Client();   
            try
            {

                result = _clients.Where(m => m.Id == CourseId).First();
                result = _clients.Remove(result).Entity;
                _context.SaveChanges();

            }
            catch (Exception ex)
            {
                Console.Write("An error occured");

            }
            return result;


        }










    }
}
































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































































